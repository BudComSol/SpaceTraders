<p>L'impianto solare genera energia, che viene utilizzata per produrre le tre risorse principali. Perché questa produzione avvenga effettivamente, l'energia utilizzata nella produzione non può superare l'energia generata dall'impianto. Quando il mix di produzione supera l'energia disponibile, i numeri Energia nel menu diventano rossi. Questo deve essere evitato.</p>
